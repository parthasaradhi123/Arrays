
public class AdvancedLoop {

	public static void main(String[] args) 
	{
		int[] a = {10,20,30,40,50};
		
		for(int ele:a) // here first a will start the execution and give value to the element
			           // and then that element goes to body then again the control goes to a.
		{
			System.out.println(ele);
		}

	}

}
